public class Student{
    String name;
    String email;
    String school;
    String age;
    String phone;
    String graduationDate;
    
    Student(String name, String email, String school, String age, String phone, String graduationDate){
        this.name = name;
        this.email = email;
        this.school = school;
        this.age = age;
        this.phone = phone;
        this.graduationDate = graduationDate;

    }
        
}
